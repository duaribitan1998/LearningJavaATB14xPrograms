public class Task_Table_Of_9_Using_PrintF {
    public static void main(String[] args) {
    int table = 9;

        System.out.println("Table Of 9 Using PrintF");
        System.out.printf("%d*1=%d", table, table*1).println();
        System.out.printf("%d*2=%d", table, table*2).println();
        System.out.printf("%d*3=%d", table, table*3).println();
        System.out.printf("%d*4=%d", table, table*4).println();
        System.out.println("....");
        System.out.printf("%d*11=%d", table, table*11).println();
        System.out.printf("%d*12=%d", table, table*12).println();
    }
}
