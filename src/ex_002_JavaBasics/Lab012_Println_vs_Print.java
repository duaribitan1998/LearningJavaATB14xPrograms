package ex_002_JavaBasics;

public class Lab012_Println_vs_Print {
    public static void main(String[] args) {
        // (Println) will print line by line or on next line //
        // ln --> New Line //
        System.out.println("Hi");
        System.out.println("Bye");
        // But (Print) will print the output on same line without space
        System.out.print("Hi");
        System.out.print("Bye");
    }
}
