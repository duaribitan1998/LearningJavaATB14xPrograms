package ex_004_Operators;

public class Lab0040_Relational_Operators {
    public static void main(String[] args) {
        //Less than <
        // Greater than >
        // >= Greater or Equal
        // == Equal to (But Checking)
        // != Not equal
        int a = 15;
        int b = 7;
        boolean c = a < b ;
        System.out.println(c);
        int  age_of_Bitan = 27;
        int age_of_Jui = 25;
        boolean result = age_of_Bitan >= age_of_Jui;
        System.out.println(result);

        // Relational Operator always give Boolean Results
    }
}
