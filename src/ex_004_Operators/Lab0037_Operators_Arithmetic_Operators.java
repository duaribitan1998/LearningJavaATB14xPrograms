package ex_004_Operators;

public class Lab0037_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 19;
        int b = 2;
        // Arithmetic Operator follows Bodmas

        System.out.println(a*b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a+b);
        System.out.println("a+b"); // This is String because of Double Quote ""
    }
}
